func populateStructFromYaml(t testing.TB, fPath string, val interface{}) {
	yamlFile, err := ioutil.ReadFile(fPath)
	if err != nil {
		t.Fatal(err)
	}
	if err := yaml.Unmarshal(yamlFile, val); err != nil {
		t.Fatalf("Failed to unmarshal: %v", err)
	}
}
